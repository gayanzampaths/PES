    private void viewDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewDataMouseClicked
        // TODO add your handling code here:
        if(datePick.getDate()!=null){
            if(this.f.getFilePath()!=null){
                try {
                    FileInputStream fileInputStream = new FileInputStream(f.getFilePath());
                    XSSFWorkbook wb = new XSSFWorkbook(fileInputStream);
                    XSSFSheet sheet = wb.getSheetAt(0);
                    Row row;
                    DataFormatter formatter = new DataFormatter();

                    row = sheet.getRow(0);

                    DefaultTableModel model = new DefaultTableModel();
                    teams.setModel(model);
                    model.addColumn("Operations");

                    for(int i=1; i<row.getLastCellNum(); i++){
                        model.addColumn(formatter.formatCellValue(row.getCell(i)));
                    }

                    String[] data = new String[row.getLastCellNum()];
                    for(int i=3; i<= sheet.getLastRowNum(); i++){
                        row = sheet.getRow(i);
                        for(int j=0; j<row.getLastCellNum();j++){
                            data[j] = formatter.formatCellValue(row.getCell(j));
                        }
                        model.addRow(data);
                    }

                } catch (IOException e) {
                    JOptionPane.showMessageDialog(null, "File Upload Failed!", "Failed", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Please Select File!", "Failed", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please Select date first!", "Failed", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_viewDataMouseClicked

    private void filePathMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_filePathMouseClicked
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Excel file", "xls", "xlsx");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            File selectFile = file.getSelectedFile();
            String path = selectFile.getAbsolutePath();
            filePath.setText(path);
            this.f.setFilePath(path);
        }else if(result == JFileChooser.CANCEL_OPTION){
            System.out.println("No File Selected!");
        }
    }//GEN-LAST:event_filePathMouseClicked

    private void uploadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_uploadMouseClicked
        // TODO add your handling code here:
        if(datePick.getDate()!=null){
            if(this.f.getFilePath()!=null){
                SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd");
                String date = sdf.format(datePick.getDate());
                //System.out.println(date);
                
                if(this.dbctrl.importTeams(f,date)){
                    JOptionPane.showMessageDialog(null, "Attendence Detail File is Successfully Uploaded!","Upload Complete",JOptionPane.PLAIN_MESSAGE);
                    this.filePath.setText("Select File");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Please Selct File First!","File Not Selected",JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please set Date First!", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_uploadMouseClicked

