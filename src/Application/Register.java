    private boolean checkFields() {
        if(!epfno.getText().isEmpty() && !username.getText().isEmpty() && desig.getSelectedIndex()!=0 && !pwd.getText().isEmpty() && !cpwd.getText().isEmpty()){
            if(pwd.getText().equals(cpwd.getText())){
                return true;
            }else{
                JOptionPane.showMessageDialog(null, "Password and Confirm Password are not match!", "Password Not match", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please fill all Fields before Registration!", "Empty Fields", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    }
