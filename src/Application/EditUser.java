    private void loadData() {
        this.user = this.dbctrl.getUserData(this.epf.getText());
        if(this.user!=null){
            DesgDep dp = new DesgDep();
            this.cd.setText(dp.desig(this.user.getDesignation()));
            this.cdep.setText(dp.Depart(this.user.getDepartment()));
        }else{
            JOptionPane.showMessageDialog(null, "User Not Found!", "Not Found!", JOptionPane.WARNING_MESSAGE);
        }
    }
