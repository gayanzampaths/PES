    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        // TODO add your handling code here:
        if(this.dep.getSelectedIndex()!=0 && this.desg.getSelectedIndex()!=0){
            this.user.setEpfNo(this.epf.getText());
            this.user.setDesignation(this.desg.getSelectedIndex());
            this.user.setDepartment(this.dep.getSelectedIndex());
            if(this.dbctrl.updateEmployeeDetails(this.user)){
                JOptionPane.showMessageDialog(null, "Update Complete!", "Complete!", JOptionPane.PLAIN_MESSAGE);
                loadData();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please load required Profile and select relevent new Information before Updating", "Update Error!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_updateMouseClicked

